        <p>We are a support group for people living with chronic pain in the Boston, MA area and surrounding New England area.  Our group was founded in April, 2000 and has been meeting continuously year round since then. Once a month, we meet to discuss the challenges of living with chronic pain and offer support to each other. At our meetings, we can also hear updates on research and therapies from physicians and other professionals in the area. </p>

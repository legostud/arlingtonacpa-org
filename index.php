        <p>We are a support group for people living with chronic pain from any condition, injury or diagnosis. Our group was founded in April, 2000 and has been meeting continuously all year around since then. Once a month, we meet to discuss the challenges of living with chronic pain and offer support to each other. At our meetings, we also invite guest speakers to present their updates on research and therapies.</p>
